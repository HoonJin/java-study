package com.hoonjin.study.java.start.variable.ex;

public class VarEx1Question {

    public static void main(String[] args) {
        System.out.println(4 + 3);
        System.out.println(4 - 3);
        System.out.println(4 * 3);

        // 4, 3을 다른 숫자로 변경할 수 있도록 num1, num2 를 사용하도록 변경해보자
        int num1 = 4;
        int num2 = 3;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
    }
}
